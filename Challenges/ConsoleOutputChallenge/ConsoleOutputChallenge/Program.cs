using System;

namespace ConsoleOutputChallenge
{
    class Program
    {
        static void Main(string[] args)
        {
            int x = 15;

            if (x > 5)
            {
                Console.WriteLine("X is greater than 5");
            }
            if (x <= 15)
            {
                Console.WriteLine("X is lesser than or equal to 15");
            }
            Console.ReadLine();
        }
    }
}
